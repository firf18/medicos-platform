/**
 * Cache configuration
 */
interface CacheConfig {
  name: string;
  maxAge: number; // in milliseconds
  maxEntries: number;
  strategy: 'cacheFirst' | 'networkFirst' | 'staleWhileRevalidate' | 'networkOnly' | 'cacheOnly';
  patterns: string[];
}

